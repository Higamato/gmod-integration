hook.Add("PlayerInitialSpawn", "SquishLogs:Log:Base", function(ply)
    SquishLog:New()
        :SetCategory("Player Connection")
        :AddFragment(ply)
        :AddFragment(' has joined the server!')
        :Send()
end)

hook.Add("PlayerSay", "SquishLogs:Log:Base", function(ply, text)
    SquishLog:New()
        :SetCategory("Player Chat")
        :AddFragment(ply)
        :AddFragment('said "')
        :SetFragmentColor(Color(200, 0, 200))
        :AddFragment(text)
        :SetFragmentColor()
        :AddFragment('"')
        :Send()
end )